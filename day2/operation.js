let n = prompt("enter your day: ");
switch (n) {
    case "monday":
    case "tuesday":
        console.log("football");    
        break;
    case wednesday:
    case thursday:
        console.log("cricket");
        break;
    case friday:
    case saturday:
        console.log("basketball");
        break;
    default:
        console.log("no game");  // this will be executed when no other case matches.
        break;
}